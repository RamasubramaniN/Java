package com.subex.ramasubramani;

public class TreeNode
{
	int x;
	TreeNode[] child;

	public int getX()
	{
		return x;
	}

	public void setX( int x )
	{
		this.x = x;
	}

	public TreeNode[] getChild()
	{
		return child;
	}

	public void setChild( TreeNode[] child )
	{
		this.child = child;
	}
}
